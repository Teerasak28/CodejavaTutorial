public class whileLoop {
    public static void main(String[] args) {
        for(int i = 1; i <= 100; i++){
            System.out.println(+i);
        }
        int iwhile = 1;
        while (iwhile <= 100) {
            System.out.println("result in whileLoop : "+iwhile);
            iwhile += 2 ;
        }
    }
    
}
